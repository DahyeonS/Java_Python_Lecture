package datatypeex;

public class IntVar {

	public static void main(String[] args) {
		
//		변수 선언과 초기화
		int i = 1;
		long l = 1L; // 1l 소문자 가능
		
//		출력: 주의 => 변수명과 문자열은 다름
		System.out.println("i");
		System.out.println("l");
		System.out.println("====");
		System.out.println(i);
		System.out.println(l);
		
	}

}

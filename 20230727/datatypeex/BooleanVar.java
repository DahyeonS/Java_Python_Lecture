package datatypeex;

public class BooleanVar {

	public static void main(String[] args) {
		
//		변수 선언과 초기화
		boolean t1 = true;
		boolean f1 = false;
		
//		출력
		System.out.println(t1);
		System.out.println(f1);
		
//		비교 연산
		t1 = 1 == 1;
		f1 = "A" == "B";
		
//		출력
		System.out.println(t1);
		System.out.println(f1);
		
	}

}
